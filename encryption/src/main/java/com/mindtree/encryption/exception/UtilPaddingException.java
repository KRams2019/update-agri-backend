package com.mindtree.encryption.exception;

public class UtilPaddingException extends EncryptionUtilException {
	private static final long serialVersionUID = 1L;

	public UtilPaddingException() {
		super();
	
	}

	public UtilPaddingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public UtilPaddingException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public UtilPaddingException(String message) {
		super(message);
	
	}

	public UtilPaddingException(Throwable cause) {
		super(cause);
	
	}
	
}
