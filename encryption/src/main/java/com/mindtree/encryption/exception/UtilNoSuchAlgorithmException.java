package com.mindtree.encryption.exception;

public class UtilNoSuchAlgorithmException extends EncryptionUtilException 
{
	private static final long serialVersionUID = 1L;

	public UtilNoSuchAlgorithmException() {
		super();
	
	}

	public UtilNoSuchAlgorithmException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public UtilNoSuchAlgorithmException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public UtilNoSuchAlgorithmException(String message) {
		super(message);
	
	}

	public UtilNoSuchAlgorithmException(Throwable cause) {
		super(cause);
	
	}
	
}
