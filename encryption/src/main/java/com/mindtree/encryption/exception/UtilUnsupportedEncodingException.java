package com.mindtree.encryption.exception;

public class UtilUnsupportedEncodingException extends EncryptionUtilException {
	private static final long serialVersionUID = 1L;

	public UtilUnsupportedEncodingException() {
		super();
	
	}

	public UtilUnsupportedEncodingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public UtilUnsupportedEncodingException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public UtilUnsupportedEncodingException(String message) {
		super(message);
	
	}

	public UtilUnsupportedEncodingException(Throwable cause) {
		super(cause);
	
	}
	
}
